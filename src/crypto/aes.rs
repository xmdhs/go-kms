// ---------- GF(2^8) helpers ----------

pub fn galois_mult(mut a: u8, mut b: u8) -> u8 {
    let mut p: u8 = 0;
    for _ in 0..8 {
        if b & 1 != 0 {
            p ^= a;
        }
        let hi_bit = a & 0x80;
        a = a.wrapping_shl(1);
        if hi_bit != 0 {
            a ^= 0x1b;
        }
        b >>= 1;
    }
    p
}

fn build_mul_table(mult: u8) -> [u8; 256] {
    let mut table = [0u8; 256];
    let mut i = 0usize;
    while i < 256 {
        table[i] = galois_mult(i as u8, mult);
        i += 1;
    }
    table
}

pub fn mul_table(mult: u8) -> &'static [u8; 256] {
    match mult {
        2 => mul2(),
        3 => mul3(),
        9 => mul9(),
        11 => mul11(),
        13 => mul13(),
        14 => mul14(),
        _ => panic!("unsupported multiplier"),
    }
}

