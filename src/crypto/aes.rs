// ---------- AES key expansion (matches Go reference) ----------

pub fn expand_key(key: &[u8], size: usize, expanded_key_size: usize) -> Vec<u8> {
    let mut expanded = vec![0u8; expanded_key_size];
    expanded[..size].copy_from_slice(&key[..size]);
    let mut current_size = size;
    let mut rcon_iteration: usize = 1;

    while current_size < expanded_key_size {
        let mut t = [0u8; 4];
        t.copy_from_slice(&expanded[current_size - 4..current_size]);

        if current_size % size == 0 {
            // Rotate.
            let (t0, t1, t2, t3) = (t[0], t[1], t[2], t[3]);
            t[0] = t1;
            t[1] = t2;
            t[2] = t3;
            t[3] = t0;
            // SubBytes.
            for i in 0..4 {
                t[i] = SBOX[t[i] as usize];
            }
            t[0] ^= RCON[rcon_iteration];
            rcon_iteration += 1;
        }
        if size == 32 && current_size % size == 16 {
            for i in 0..4 {
                t[i] = SBOX[t[i] as usize];
            }
        }

        for i in 0..4 {
            expanded[current_size] = expanded[current_size - size] ^ t[i];
            current_size += 1;
        }
    }
    expanded
}

pub fn build_round_keys(expanded: &[u8], rounds: usize) -> Vec<[u8; 16]> {
    let mut out = vec![[0u8; 16]; rounds + 1];
    for r in 0..=rounds {
        let offset = r * 16;
        for i in 0..4 {
            for j in 0..4 {
                out[r][j * 4 + i] = expanded[offset + i * 4 + j];
            }
        }
    }
    out
}

pub fn apply_v6_round_patches(round_keys: &mut [[u8; 16]]) {
    for &(round, patch) in V6_ROUND_PATCHES.iter() {
        round_keys[round][0] ^= patch;
    }
}

// ---------- AES primitive operations (state = 16 bytes, column-major matching Go) ----------

pub fn sub_bytes(state: &mut [u8; 16], inv: bool) {
    let table = if inv { &RSBOX } else { &SBOX };
    for b in state.iter_mut() {
        *b = table[*b as usize];
    }
}

pub fn shift_rows(state: &mut [u8; 16], inv: bool) {
    if inv {
        let (a, b, c, d) = (state[4], state[5], state[6], state[7]);
        state[4] = d;
        state[5] = a;
        state[6] = b;
        state[7] = c;
        let (a, b, c, d) = (state[8], state[9], state[10], state[11]);
        state[8] = c;
        state[9] = d;
        state[10] = a;
        state[11] = b;
        let (a, b, c, d) = (state[12], state[13], state[14], state[15]);
        state[12] = b;
        state[13] = c;
        state[14] = d;
        state[15] = a;
        return;
    }
    let (a, b, c, d) = (state[4], state[5], state[6], state[7]);
    state[4] = b;
    state[5] = c;
    state[6] = d;
    state[7] = a;
    let (a, b, c, d) = (state[8], state[9], state[10], state[11]);
    state[8] = c;
    state[9] = d;
    state[10] = a;
    state[11] = b;
    let (a, b, c, d) = (state[12], state[13], state[14], state[15]);
    state[12] = d;
    state[13] = a;
    state[14] = b;
    state[15] = c;
}

fn mix_column(state: &mut [u8; 16], i0: usize, i1: usize, i2: usize, i3: usize, inv: bool) {
    let (a0, a1, a2, a3) = (state[i0], state[i1], state[i2], state[i3]);
    if inv {
        let m9 = mul9();
        let m11 = mul11();
        let m13 = mul13();
        let m14 = mul14();
        state[i0] = m14[a0 as usize] ^ m9[a3 as usize] ^ m13[a2 as usize] ^ m11[a1 as usize];
        state[i1] = m14[a1 as usize] ^ m9[a0 as usize] ^ m13[a3 as usize] ^ m11[a2 as usize];
        state[i2] = m14[a2 as usize] ^ m9[a1 as usize] ^ m13[a0 as usize] ^ m11[a3 as usize];
        state[i3] = m14[a3 as usize] ^ m9[a2 as usize] ^ m13[a1 as usize] ^ m11[a0 as usize];
        return;
    }
    let m2 = mul2();
    let m3 = mul3();
    state[i0] = m2[a0 as usize] ^ a3 ^ a2 ^ m3[a1 as usize];
    state[i1] = m2[a1 as usize] ^ a0 ^ a3 ^ m3[a2 as usize];
    state[i2] = m2[a2 as usize] ^ a1 ^ a0 ^ m3[a3 as usize];
    state[i3] = m2[a3 as usize] ^ a2 ^ a1 ^ m3[a0 as usize];
}

pub fn mix_columns(state: &mut [u8; 16], inv: bool) {
    mix_column(state, 0, 4, 8, 12, inv);
    mix_column(state, 1, 5, 9, 13, inv);
    mix_column(state, 2, 6, 10, 14, inv);
    mix_column(state, 3, 7, 11, 15, inv);
}

pub fn add_round_key(state: &mut [u8; 16], round_key: &[u8; 16]) {
    for i in 0..16 {
        state[i] ^= round_key[i];
    }
}

// Convert 16 raw bytes -> column-major state (Go: state[i+j*4] = input[i*4+j]).
pub fn state_from_input(input: &[u8]) -> [u8; 16] {
    let mut state = [0u8; 16];
    for i in 0..4 {
        for j in 0..4 {
            state[i + j * 4] = input[i * 4 + j];
        }
    }
    state
}

pub fn state_to_output(state: &[u8; 16], out: &mut [u8]) {
    for i in 0..4 {
        for j in 0..4 {
            out[i * 4 + j] = state[i + j * 4];
        }
    }
}
